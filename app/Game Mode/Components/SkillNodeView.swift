import SwiftUI

// Описываем новую структуру SkillNodeView
// Здесь struct используется для создания одного элемента на экране: узла навыка в паутинке.
struct SkillNodeView: View {

    // var — это переменная или свойство структуры.
    // Здесь мы создаём свойство skill: Skill
    // Это значит: SkillNodeView получает на вход один конкретный объект Skill, чтобы показать его данные.
    var skill: Skill

    // body — специальное свойство для любого View.
    // Здесь мы описываем, как будет выглядеть этот узел навыка на экране.
    var body: some View {
        
        // skill.name — это доступ к полю "name" внутри переданного объекта Skill.
        Text(skill.name)

        // То есть: на экране мы просто пишем название навыка (например, "Planning" или "Stick-to-it'ness").
    }
}
