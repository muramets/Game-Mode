import SwiftUI

// Описываем новую структуру под названием SpiderWebView
// struct — это способ объединить несколько свойств и методов в один логичный объект.
// Здесь struct описывает экран (View), а не данные.
// View в SwiftUI — это просто описание, "как должен выглядеть кусочек экрана", без логики обработки данных.
struct SpiderWebView: View {

    // var — это способ создать переменную или свойство внутри структуры.
    // body — специальное свойство для любого View.
    // Оно говорит SwiftUI: "Вот как должен выглядеть мой экран."
    // Тип свойства: some View — это значит "тут будет какой-то элемент интерфейса (View)".
    var body: some View {
        Text("Spider Web Placeholder")
    }

    // Пример из реальной жизни:
    // Представь, что struct SpiderWebView — это как рисунок на бумаге: мы описали, что хотим нарисовать (пока просто текст).
    // А var body — это сама бумага с текстом "Spider Web Placeholder" посередине.
}

